
public class DryRun {

    public static void main(String[] args) {
        int a = 73;
        int b = -61;
        System.out.println(~a);
        System.out.println(~b);
        System.out.println(a^b);
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(b>>>2);
        System.out.println(b>>2);
        System.out.println(b<<2);


    }
}
